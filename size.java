/*This program gives size and range of data types
 * to know size and range give commamd as javap java.lang.(prefered datatype class eg:Integer)
 */
public class size {
    public static void main(String[] args) {
        System.out.println("integer max value "+Integer.MAX_VALUE+ " integer min value "+Integer.MIN_VALUE+ " integer size "+Integer.SIZE);
        System.out.println("Float max value "+Float.MAX_VALUE+ " Float min value "+Float.MIN_VALUE+ " Float size "+Float.SIZE);
        System.out.println("double max value "+Double.MAX_VALUE+ " double min value "+Double.MIN_VALUE+ " double size "+Double.SIZE);
    
    }
}
