/*Simple program for demonstration of arthimetic operations */
public class arthimetic {
    public static void main(String[] args) {
        int a=5,b=9;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(a/b);
        /* modulus works on float type data */
        float f=16.3f,g=3.1f;
        System.out.println(f%g);
    }
}
